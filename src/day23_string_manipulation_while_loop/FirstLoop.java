package day23_string_manipulation_while_loop;

public class FirstLoop {
    public static void main(String[] args) {
        int i = 1;
        if (i <= 5){
            System.out.println(i);
        }
        int apples = 0;
        while(apples <= 10){
            System.out.println("apples-> " + apples);
            apples++;
        }
        System.out.println("apples = " + apples);

    }
}
