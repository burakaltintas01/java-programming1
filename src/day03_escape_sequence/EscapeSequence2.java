package day03_escape_sequence;

public class EscapeSequence2 {
    public static void main(String[]args){
        System.out.println("\"iphone case\", \"iphone\", \"ps5\"");
        System.out.println("\"laptop\", \"case\", \"java\"");
        System.out.println("\"tv\", \"blanket\", \"coffee\"");
        System.out.println("\"iphone\", \"iphone case\",\n \"monitor\", \"xbox\"");
        System.out.println("   ^    \n /   \\ \n |   |");
        System.out.println(" ----");
    }
}
