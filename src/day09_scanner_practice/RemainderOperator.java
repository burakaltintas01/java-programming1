package day09_scanner_practice;

public class RemainderOperator {
    public static void main(String[] args) {
        System.out.println(10/3);
        System.out.println(10%3); //3+3+3=9 what do we need to add to make it 10? its 1.
        System.out.println(10/5);
        System.out.println(20/7);
        System.out.println(20%7); // 7+7 is 14, what do we need to make it 20? its 6.

    }
}
