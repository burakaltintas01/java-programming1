package day12_conditional_statements;

public class CompareChars {
    public static void main(String[] args) {
        char letter1 = 'a';
        char letter2 = 'j';
        System.out.println(letter1 == letter2);
        System.out.println(letter1 >= letter2);
        System.out.println(letter1 <= letter2);

        char grade = 'a';
        boolean pass = grade <= 'd';
        System.out.println("Did you pass the exam? " + pass);






    }
}
